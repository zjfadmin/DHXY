package come.tool.Scene.RC;

import org.come.model.Boos;

public class BBBrush {

	private int d;//当前次数
	private int z;//总次数
	private Boos boos;
	
	public int getD() {
		return d;
	}
	public void setD(int d) {
		this.d = d;
	}
	public int getZ() {
		return z;
	}
	public void setZ(int z) {
		this.z = z;
	}
	public Boos getBoos() {
		return boos;
	}
	public void setBoos(Boos boos) {
		this.boos = boos;
	}
}
